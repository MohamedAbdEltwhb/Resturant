package com.example.mm.resturant.models.data;

public final class DrinksInfo {

    private int mDrinksImage;
    private String mDrinksName;

    public DrinksInfo(int mDrinksImage, String mDrinksName) {
        this.mDrinksImage = mDrinksImage;
        this.mDrinksName = mDrinksName;
    }

    public int getmDrinksImage() {
        return mDrinksImage;
    }

    public String getmDrinksName() {
        return mDrinksName;
    }
}
